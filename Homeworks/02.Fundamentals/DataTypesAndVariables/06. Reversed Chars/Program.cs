using System;

namespace _06._Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            string one = Console.ReadLine();
            string two = Console.ReadLine();
            string three = Console.ReadLine();
            string output = "";

            output += three + " " + two + " " + one;
            Console.WriteLine(output);
        }
    }
}
